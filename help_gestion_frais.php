<h1>Aide de la page de gestion des frais</h1>
<p>
Le RCKL dispose d'un système de gestion des dépenses entre les membres, notamment dans le cas de sorties communes.
Il évite les calculs fastidieux, les problèmes de monnaie ou les post-it «pour se rappeler» liés aux remboursements immédiats.
Le système est théoriquement prévu pour que jamais personne n'ait à rembourser «directement» de l'argent, ce qui est effectivement le cas à partir du moment où les dépenses sont à peu près équilibrées entre les membres sur le moyen terme.
Le système de déclaration des frais se révèle très pratique, cependant personne n'est obligé de l'utiliser: faites le avec des gens que vous connaissez bien et auxquels vous faites confiance.
</p>
<p>
La page de gestion des frais vous permet de consulter votre compte, suivre les déclarations de frais qui vous concernent, en particulier les réfuter ou les accepter, et effectuer vos propres déclarations ou les annuler si elles sont contestées ou erronées.
</p>

<h2>Principe général de la gestion des frais</h2>

Il y a trois phases dans la gestion des frais:
<ol>
  <li> <b>La déclaration.</b>
  Elle s'effectue par le bouton prévu à cet effet en haut de la page. Des instructions détaillées sont accessibles dans la page de déclaration proprement dite.
  Le principe essentiel qu'il faut retenir est le suivant: c'est toujours la personne <em>qui a dépensé l'argent</em> qui effectue la déclaration.
  Ce faisant elle indique toutes les personnes qui ont bénéficié de cette dépense, <em>y compris elle même, le cas échéant</em>.
  Le système divise alors la somme dépensée par le nombre de bénéficiaires et débite chacun d'eux de la part résultante, tandis qu'il crédite l'auteur de la dépense de la somme totale dépensée.
  Toutes les personnes concernées par une déclaration sont prévenues par mail automatiquement.
  </li>
  <li> <b>La phase de validation.</b>
  Une fois la déclaration effectuée, elle entre en phase de validation pendant 20 jours.
  Pendant cette phase, les bénéficiaires désignés peuvent faire opposition. 
  Dans ce cas, l'auteur est prévenu par mail, et une négociation devrait être entreprise pour régler le «conflit», à l'issue de quoi la personne ayant fait opposition peut se rétracter en acceptant la déclaration, ou alors l'auteur peut annuler sa déclaration sans aucune conséquence pour personne.
  </li>
  <li> <b>La prise en compte.</b>
  Quand la période de validation arrive à échéance, la déclaration de frais est examinée.
  Si elle a été contestée par quelqu'un, elle est automatiquement supprimée, sans effet, et l'auteur en est averti par mail.
  Dans le cas contraire, les sommes prévues sont créditées ou débitées des comptes.
  Après la prise en compte, l'auteur possède encore la possibilité d'annuler sa déclaration, auquel cas les sommes transférées sont annulées.
  </li>
</ol>

<h2>Descriptions des éléments affichés</h2>

<h3>Bilan personnel global</h3>
<p>
Les membres qui apparaissent ici sont uniquement ceux avec qui vous avez déjà eu des dépenses, qu'ils aient été bénéficiaires d'une de vos dépenses, ou que vous ayez été bénéficiaire d'une des leurs.
</p>
<p>
Les sommes indiquées représentent le crédit/débit total et global depuis l'inscription au RCKL.
Les sommes négatives sont des dettes.
La personne ayant le plus grand crédit est indiquée en vert, celle ayant le plus de débit est en rouge. 
En cas de remboursement, la personne rouge devrait donner de l'argent à la personne verte pour minimiser les sommes extrêmes.
<br/>
Il est normal que l'addition de toutes les sommes ne soit pas égale à 0. 
En effet les membres indiqués ont pu avoir des dépenses avec d'autres membres que ceux visualisés ici et ceci a modifié leur solde.
</p>

<h3>Dépenses en cours de validation</h3>
<p>
Cette partie indique les dépenses en cours de validation et qui vous concernent: soit parce que vous avez effectué la déclaration, soit parce qu'un autre membre en a effectué une et vous a indiqué parmi les bénéficiaires.
</p>
<ul>
<li> Si vous avez effectué la déclaration, vous pouvez cliquer sur la croix <img class="icon" src="ICONS/b_drop.png"/> située à côté de la somme dépensée pour annuler complètement cette déclaration.
</li>
<li> Si vous êtes mentionné parmi les bénéficiaires d'une dépense, vous pouvez cliquer sur la croix <img class="icon" src="ICONS/b_drop.png"/> située à côté de votre nom afin de réfuter la dépense (vous apparaissez alors en rouge), ou alors sur le symbole <img class="icon" src="ICONS/b_add.png"/> pour vous rétracter et accepter la dépense (vous apparaissez alors en vert).
</li>
</ul>

<h3>Détail des dépenses passées vous concernant</h3>
<p>
Cette dernière partie constitue l'historique des dépenses qui vous concernent et qui ont été validées et prises en compte.
Vous avez toujours la possibilité d'annuler l'une d'elles en cliquant sur la croix <img class="icon" src="ICONS/b_drop.png"/> située à côté de la somme dépensée. 
Les comptes seront alors re-crédités ou re-débités selon le cas, comme si aucune déclaration n'avait jamais été faite.
</p>
