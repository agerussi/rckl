<h1>PRÉSENTATION</h1>
Le <b>groupe loisirs</b> est une structure intégrée au club de canoë-kayak de Saint Laurent Blangy et regroupe des personnes, pour la plupart majeures, qui souhaitent pratiquer le kayak (sous toutes ses formes) dans un but de détente et de découverte.
Le <b>groupe loisirs</b> ne dispose pas d'un cadre diplômé spécialement rattaché: c'est un groupe qui s'auto-gère, mutualise les ressources, et chaque membre du groupe progresse au contact des autres membres plus expérimentés.
<p>
Les activités du <b>groupe loisirs</b> se déclinent sous diverses formes:
<ul>
<li> des séances en eau vive dans le cadre de la base nautique de SLB: kayak principalement, mais aussi canoë, raft, nage en eau vive...</li>
<li> des sorties allant d'une demi-journée à plusieurs jours, au minimum une fois par mois;</li>
<li> des séances en eau plate: travail technique, kayak-polo, kayak de descente ou de course en ligne; </li>
<li> des séances d'esquimautage à la piscine d'Avion, en semaine, de 21h à 22h30, pendant la saison hivernale.</li>
</ul>
Un membre du <b>groupe loisirs</b> a également la possibilité de profiter des autres activités proposées aux membres du club: accès à la salle de musculation,  VTT, footing.
</p>
<h4>Horaires</h4>
Le meeting habituel du groupe est le samedi après midi, de 14h à 17h environ. Mais il y a possibilité de venir en semaine également.

<h4>Organisation des séances</h4>
Les séances ne sont pas organisées! 
Le groupe loisirs est exactement ce que ses membres en font. 
Et à l'heure actuelle, c'est une bande de copains qui gravitent autour d'une même passion.
Nous aimons partager, donner des conseils, ou «encadrer» les débutants, mais il faut comprendre une chose: le groupe loisirs n'est pas un prestataire de services, <em>chaque pratiquant est responsable de lui-même, des choses qu'il apprend, des sorties auxquelles il participe</em>.
Si vous restez seul dans votre coin, personne ne viendra vous déranger.
Ne nous demandez pas de remplacer votre propre motivation à apprendre le kayak.

<h4>Cotisation</h4>
La cotisation à la FFCK s'étend de janvier à fin décembre.
Si toutefois vous vous inscrivez en cours d'année, il est possible de profiter d'un prix «spécial»: renseignez-vous.
Le prix pour une saison est légèrement variable d'années en années, mais à titre indicatif:
<ul>
<li> un chèque d'environ 100 euros pour la licence FFCK et l'inscription au club;</li>
<li> un chèque d'environ 50 euros pour la base nautique (accès à l'eau vive).</li>
</ul>
Vous pouvez trouver les documents d'inscription détaillés (à imprimer) dans la rubrique <a href="documents.php?menu">documents</a>.

<h4>Conseils pour les nouveaux arrivants n'ayant jamais fait de kayak (et les autres...)</h4>
Vous avez pris votre décision: à la rentrée vous faites du kayak! 
Voici quelques conseils pour une rentrée réussie au groupe loisirs:
<ul> 
  <li>Si vous vous inscrivez en septembre, <em>soyez près début septembre</em>: et pour cela faites vos démarches fin août. 
En effet septembre est un mois qui reste en général doux et relativement chaud, et il serait dommage de rater ces premières séances agréables. 
Plus vous attendrez et plus l'automne se fera sentir, le temps maussade en a découragé plus d'un(e).
De plus le groupe est souvent en sortie fin septembre, donc si vous ratez le début du mois, cela vous reporte probablement début octobre.</li>
  <li><em>Faites le maximum pour venir à 3 ou 4 séances d'affilée au début</em>: cela facilite votre intégration et votre «prise en main» par quelqu'un du groupe, par exemple par un travail technique en eau plate afin d'acquérir les bases.</li>
  <li><em>La question de l'équipement</em>: que faut-il acheter ? 
Le club vous permet d'emprunter le matériel indispensable: gilet, jupe, casque, pagaie et kayak. 
Il est probable qu'assez rapidement vous désiriez acheter certains de ces éléments (notamment une jupe néoprène), mais on peut utiliser ce matériel sans problème pendant les deux premières années. 
<br/>
Reste donc à votre charge l'équipement corporel. 
Voici quelques suggestions pour débuter:
  <ul>
    <li>Une combinaison néoprène jambes longues, pas trop épaisse (pas une combinaison de plongée). 
L'idéal est de la prendre sans manches (bretelles), mais on peut également la prendre à manches longues pour avoir plus chaud et se passer d'un kway dans un premier temps.</li>
    <li>Un top thermique à porter à même la peau sous la combinaison (indispensable si votre combinaison est à bretelles), à manches longues si vous voulez avoir plus chaud.</li>
    <li>Des bottillons néoprènes avec une semelle raisonnable pour marcher dans «la brousse» ou les rochers, mais la plus souple possible, et tenant bien au pied.</li>
<em>Tout cela se trouve facilement dans les magasins de sports traditionnels type d*c*t*l*n.</em>
    <li>Si vous avez opté pour une combinaison à bretelles, il vous faudra encore un kway «kayak» pour se protéger du vent et des éclaboussures.
  Pour cela il faudra probablement commander par correspondance dans un magasin spécialisé. Les kways varient selon leur solidité et leur étanchéité:
     <ul>
       <li>le kway basique possède la taille, les poignets et le cou fermés par du néoprène: coupe le vent, pas franchement étanche si les projections d'eau sont régulières.</li>
       <li>le kway «standard» possède une double cheminée à la taille (système pour être étanche avec la jupe), des poignets latex: bien étanche sauf si l'on nage et/ou esquimaute très souvent.</li>
       <li>le kway «haut de gamme» possède en plus un cou latex et peut-être davantage de «couches» et est plus solide: a priori totalement étanche.</li>
     </ul>
Pour débuter, on peut conseiller un kway basique ou une entrée de gamme «standard». 
Cependant avoir un bon kway est le gage d'avoir chaud sur l'eau même à la mauvaise saison: on peut en effet «empiler des couches» sous le kway étanche en fonction de la température.
  </ul>
</ul>

