<h1>PRÉSENTATION</h1>

Le RCKL est un <b>réseau de proximité</b> à destination des kayakistes pratiquant l'activité en loisir sous une forme ou une autre (rivière, randonnée, mer, freestyle...), et dont les objectifs finaux incluent:
<ul>
<li>la mutualisation idées de sorties et des ressources;</li>
<li>l'accès à du matériel;</li>
<li>la création d'un réseau social indépendant, démocratique, pérenne et complémentaire des structures existantes (club, FFCK...);</li>
<li>un site performant offrant des services concrets dans l'organisation des activités liées au kayak (organisation, gestion des frais, ...);</li>
<li>l'intégration et la formation des débutants.</li>
</ul>
<p>
<u>La philosophie du RCKL, ainsi que son site web, sont en développement actif.</u>
Des informations plus détaillées viendront s'ajouter en temps et en heure.
</p>
<p>Pour le moment, si vous êtes un kayakiste possédant son matériel et que vous savez à peu près où vous mettez les pieds (par exemple si vous connaissez déjà un des membres du RCKL), vous êtes invités à vous <a href="profile_new.php">créer un compte</a> et également à vous inscrire sur <a href="liste-diffusion.php">la liste de diffusion</a>.
<br/>
Vous pourrez dès lors, entre autres, vous inscrire aux sorties existantes ou en proposer de nouvelles.
</p>
<p>Enfin, si vous n'arrivez pas à savoir si le RCKL est pour vous, n'hésitez pas <a href="mailto:rckl@free.fr">prendre contact</a>.
</p>
